document.addEventListener('DOMContentLoaded', () => {
    // 1. Navigation Scroll Effect
    const navbar = document.getElementById('navbar');
    window.addEventListener('scroll', () => {
        if (window.scrollY > 50) {
            navbar.classList.add('scrolled');
        } else {
            navbar.classList.remove('scrolled');
        }
    });

    // 2. Active Section Navigation Highlighter
    const sections = document.querySelectorAll('section, header');
    const navItems = document.querySelectorAll('.nav-item');
    
    const observerOptions = {
        root: null,
        rootMargin: '-30% 0px -50% 0px', // Trigger when section occupies the middle portion of viewport
        threshold: 0
    };

    const sectionObserver = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                const id = entry.target.getAttribute('id') || '';
                navItems.forEach(item => {
                    const href = item.getAttribute('href').substring(1);
                    if (href === id) {
                        item.classList.add('active');
                    } else {
                        item.classList.remove('active');
                    }
                });
            }
        });
    }, observerOptions);

    sections.forEach(section => {
        if (section.getAttribute('id')) {
            sectionObserver.observe(section);
        }
    });

    // 3. Mobile Hamburger Menu Toggle
    const mobileMenuBtn = document.getElementById('mobile-menu-btn');
    const mobileNavDrawer = document.getElementById('mobile-nav-drawer');
    const mobileLinks = document.querySelectorAll('.mobile-nav-item');

    if (mobileMenuBtn && mobileNavDrawer) {
        mobileMenuBtn.addEventListener('click', () => {
            mobileNavDrawer.classList.toggle('open');
            mobileMenuBtn.classList.toggle('active');
            
            // Toggle hamburger icon animation
            const spans = mobileMenuBtn.querySelectorAll('span');
            if (mobileNavDrawer.classList.contains('open')) {
                spans[0].style.transform = 'rotate(45deg) translate(5px, 5px)';
                spans[1].style.opacity = '0';
                spans[2].style.transform = 'rotate(-45deg) translate(6deg, -6deg)';
            } else {
                spans[0].style.transform = 'none';
                spans[1].style.opacity = '1';
                spans[2].style.transform = 'none';
            }
        });

        // Close drawer on link click
        mobileLinks.forEach(link => {
            link.addEventListener('click', () => {
                mobileNavDrawer.classList.remove('open');
                const spans = mobileMenuBtn.querySelectorAll('span');
                spans[0].style.transform = 'none';
                spans[1].style.opacity = '1';
                spans[2].style.transform = 'none';
            });
        });
    }

    // 4. Stats Counter Animation
    const statsSection = document.querySelector('.stats-section');
    const statNumbers = document.querySelectorAll('.stat-number');
    let animated = false;

    const animateCounters = () => {
        statNumbers.forEach(num => {
            const target = parseInt(num.getAttribute('data-target'), 10);
            let count = 0;
            const speed = target / 50; // controls the speed of counting
            
            const updateCount = () => {
                if (count < target) {
                    count = Math.ceil(count + speed);
                    if (count > target) count = target;
                    num.innerText = count + (target === 150 || target === 4 || target === 10000 ? '+' : '');
                    setTimeout(updateCount, 25);
                }
            };
            updateCount();
        });
    };

    const statsObserver = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting && !animated) {
                animateCounters();
                animated = true;
            }
        });
    }, { threshold: 0.3 });

    if (statsSection) {
        statsObserver.observe(statsSection);
    }

    // 5. Timetable Schedule Tabs Filter
    const tabBtns = document.querySelectorAll('.tab-btn');
    const scheduleCards = document.querySelectorAll('.schedule-card');

    tabBtns.forEach(btn => {
        btn.addEventListener('click', () => {
            // Set active class
            tabBtns.forEach(b => b.classList.remove('active'));
            btn.classList.add('active');

            const category = btn.getAttribute('data-tab');

            scheduleCards.forEach(card => {
                const cardCategory = card.getAttribute('data-category');
                
                if (category === 'all' || category === cardCategory) {
                    card.style.display = 'block';
                    card.style.animation = 'fadeIn 0.4s ease forwards';
                } else {
                    card.style.display = 'none';
                }
            });
        });
    });



    // 7. Blog Details Modal (Dialogue)
    const blogDialog = document.getElementById('blog-dialog');
    const blogDialogClose = document.getElementById('blog-dialog-close');
    const blogDialogBody = document.getElementById('blog-dialog-body');
    const blogProgressBar = document.getElementById('blog-reading-progress');
    const readMoreBtns = document.querySelectorAll('[data-trigger]');

    if (blogDialog && blogDialogClose && blogDialogBody) {
        // Open Modal
        readMoreBtns.forEach(btn => {
            btn.addEventListener('click', () => {
                const postId = btn.getAttribute('data-trigger');
                const post = BLOG_POSTS[postId];
                
                if (post) {
                    // Populate
                    blogDialogBody.innerHTML = `
                        <span class="dialog-category-tag">${post.category}</span>
                        <h2>${post.title}</h2>
                        <div class="dialog-meta">By ${post.author} • ${post.date} • ${post.readTime}</div>
                        <div class="dialog-article-text">
                            ${post.content}
                        </div>
                    `;
                    
                    // Reset progress bar
                    blogProgressBar.style.width = '0%';
                    
                    // Open
                    blogDialog.showModal();
                }
            });
        });

        // Close Modal
        blogDialogClose.addEventListener('click', () => {
            blogDialog.close();
        });

        // Close on clicking backdrop
        blogDialog.addEventListener('click', (e) => {
            const rect = blogDialog.getBoundingClientRect();
            const isInDialog = (
                rect.top <= e.clientY && e.clientY <= rect.top + rect.height &&
                rect.left <= e.clientX && e.clientX <= rect.left + rect.width
            );
            if (!isInDialog) {
                blogDialog.close();
            }
        });

        // Modal scroll reading progress tracker
        blogDialog.addEventListener('scroll', () => {
            const scrollTop = blogDialog.scrollTop;
            const scrollHeight = blogDialog.scrollHeight;
            const clientHeight = blogDialog.clientHeight;
            
            // Calculate percentage
            const totalScrollable = scrollHeight - clientHeight;
            if (totalScrollable > 0) {
                const scrolledPercent = (scrollTop / totalScrollable) * 100;
                blogProgressBar.style.width = `${scrolledPercent}%`;
            }
        });
    }

    // 8. Contact Form Handler
    const contactForm = document.getElementById('contact-form');
    const formStatus = document.getElementById('form-status');

    if (contactForm && formStatus) {
        contactForm.addEventListener('submit', (e) => {
            e.preventDefault();
            
            // Display sending state
            formStatus.className = 'form-status';
            formStatus.innerText = 'Sending your message to CRG Team...';
            formStatus.classList.remove('hidden');
            
            // Simulate API request
            setTimeout(() => {
                formStatus.className = 'form-status success';
                formStatus.innerText = 'Thank you! Your message has been sent successfully. The CRG ExCom team will reach out to you soon.';
                contactForm.reset();
            }, 1200);
        });
    }

    // 8b. Toastmasters Contact Form Handler
    const tmContactForm = document.getElementById('tm-contact-form');
    const tmFormStatus = document.getElementById('tm-form-status');

    if (tmContactForm && tmFormStatus) {
        tmContactForm.addEventListener('submit', (e) => {
            e.preventDefault();
            
            // Display sending state
            tmFormStatus.className = 'form-status';
            tmFormStatus.innerText = 'Sending your inquiry to CRG Toastmasters officers...';
            tmFormStatus.classList.remove('hidden');
            
            // Simulate API request
            setTimeout(() => {
                tmFormStatus.className = 'form-status success';
                tmFormStatus.innerText = 'Thank you! Your message has been sent. The CRG Toastmasters officers will contact you shortly.';
                tmContactForm.reset();
            }, 1200);
        });
    }

    // 9. Leaderboard Tab Toggle
    const leadTabBtns = document.querySelectorAll('.lead-tab-btn');
    const leadTables = document.querySelectorAll('.leaderboard-table-container');

    leadTabBtns.forEach(btn => {
        btn.addEventListener('click', () => {
            leadTabBtns.forEach(b => b.classList.remove('active'));
            btn.classList.add('active');

            const gender = btn.getAttribute('data-gender');
            leadTables.forEach(table => {
                if (table.getAttribute('id') === `lead-${gender}`) {
                    table.classList.add('active');
                } else {
                    table.classList.remove('active');
                }
            });
        });
    });

    // 10. Scroll Reveal Animations
    const revealElements = document.querySelectorAll('.scroll-reveal');
    const revealObserver = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('visible');
                revealObserver.unobserve(entry.target); // Animates only once
            }
        });
    }, {
        threshold: 0.05,
        rootMargin: "0px 0px -40px 0px"
    });

    revealElements.forEach(el => revealObserver.observe(el));

    // 11. Button Micro-interactions (shrink on click)
    const clickables = document.querySelectorAll('.btn, .btn-join, .tab-btn, .lead-tab-btn, .btn-read-more, .btn-route-action');
    clickables.forEach(el => {
        el.addEventListener('mousedown', () => {
            el.style.transform = 'scale(0.96)';
        });
        el.addEventListener('mouseup', () => {
            el.style.transform = '';
        });
        el.addEventListener('mouseleave', () => {
            el.style.transform = '';
        });
    });
});

